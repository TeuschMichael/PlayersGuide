public class TheVariableShop {

    public static void main(String[] args) {

        byte newByte = -67;
        short newShort = 11111;
        int newInt = 5;
        long newLong = 21589456879L;
        float newFloat = 21.5f;
        double newDouble = 15.321d;
        boolean newBoolean = true;
        char newChar = 'C';

        System.out.println("The list of variables are");
        System.out.println("Byte " + newByte);
        System.out.println("Short " + newShort);
        System.out.println("Integer " + newInt);
        System.out.println("Long " + newLong);
        System.out.println("Float " + newFloat);
        System.out.println("Double " + newDouble);
        System.out.println("Boolean " + newBoolean);
        System.out.println("Character " + newChar);

        newByte = -50;
        newShort = 11221;
        newInt = 7;
        newLong = 56219456879L;
        newFloat = 27.7f;
        newDouble = 14.555d;
        newBoolean = false;
        newChar = 'A';

        System.out.println("The list of new variables are");
        System.out.println("Byte " + newByte);
        System.out.println("Short " + newShort);
        System.out.println("Integer " + newInt);
        System.out.println("Long " + newLong);
        System.out.println("Float " + newFloat);
        System.out.println("Double " + newDouble);
        System.out.println("Boolean " + newBoolean);
        System.out.println("Character " + newChar);
    }
}
